fun main(){
    var a:Int = 22
    var b:Float = 2.2F
    var c:Double = 96.36
    var d:String = "HMS"
    var e:Char = 'V'

    var f:Double = a.toDouble()
    println("$f:Int to Double")

    var g:Int = b.toInt()
    println("$g:Float to Int")

    var m:String = c.toString()
    println("$m:Double to string")

    var v:String = e.toString()
    println("$v:Char to String")

}
